object Calculator {

  def add(a: Int, b: Int) : Int = a + b
  def sub(a: Int, b: Int) : Int = a - b
  def mul(a: Int, b: Int) : Int = a * b
  def div(a: Int, b: Int) : Int = a / b
  def mod(a: Int, b: Int) : Int = a % b

}
